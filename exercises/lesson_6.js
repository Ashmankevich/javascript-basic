function sumInput() {
  let arr = [];
  let value;
  if ((value = prompt("Please, enter the sum value", ""))) {
    for (
      let i = 0;
      isNaN(value) === false && value !== null && value !== "";
      i++
    ) {
      arr.push(value);
      value = prompt("It's ok, you should enter some value more", "");
    }
  }
  let sum = 0;
  for (let i = 0; i < arr.length; i++) {
    sum += +arr[i];
  }
  return sum;
}
